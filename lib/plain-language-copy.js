function applyPlainLanguage(html) {
  let out = html;
  for (const [from, to] of REPLACEMENTS) {
    if (from && out.includes(from)) {
      out = out.split(from).join(to);
    }
  }
  // Idempotent: avoid compounding "mortgage" on repeated builds (pre-approval → mortgage pre-approval).
  out = out.replace(/(?<!mortgage )pre-approval/g, 'mortgage pre-approval');
  out = out.replace(/(?<!Mortgage )Pre-approval/g, 'Mortgage pre-approval');
  out = out.replace(/(?:mortgage ){2,}pre-approval/gi, 'mortgage pre-approval');
  return out;
}

